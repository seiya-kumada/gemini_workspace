"""
GPT-4oへの入力プロンプトとレスポンスモデルの定義

このモジュールは、Azure OpenAI GPT-4oモデルに送信するプロンプトと、
期待されるレスポンスの構造を定義します。
"""

from pydantic import BaseModel


class Output(BaseModel):
    """
    GPT-4oからのレスポンスモデル

    Attributes:
        description: 画像内容の説明文
    """

    items: list[str]


# 画像とOCR結果を入力とする場合のプロンプト
INPUT_PROMPT = (
    "## 提供するもの：\n"
    "- 画像一枚を与える。\n"
    "## 前提 \n"
    "- あなたは道路付属物・電柱基礎の設計および施工図を確認する構造設計・施工管理エンジニアです。\n"
    "## 質問 \n"
    "- 画像は、「電柱基礎配筋図」です。\n"
    "- この図面から「電柱基礎の構造成立・転倒防止・施工性を最も特徴づける情報」のみを、簡潔なキーワードとして抽出し、itemsに入れてください。\n"
    "## 抽出対象 \n"
    "- 基礎形式（独立基礎、直接基礎、場所打ち、プレキャスト）\n"
    "- 基礎寸法・形状（基礎径、基礎幅、基礎高、根入れ深さ）\n"
    "- 主配筋・補強配筋（主筋径・本数、フープ筋・帯筋、底版補強筋）\n"
    "- 電柱固定・支持条件（電柱挿入方式、アンカーボルト、固定金具）\n"
    "- 転倒・引抜き対策（根入れ深さ確保、補強筋増し、支圧対策）\n"
    "- 施工・設置条件（埋戻し条件、支持地盤、施工順序、据付精度）\n"
    "## 注意点\n"
    "- 表記ゆれを正規化する。（例：「プレストレストコンクリート橋」→「PC橋」）\n"
    "- 数値は意味を持つもののみ記載（例：支間長 40m）\n"
    "- 同一内容は1回のみ抽出する。\n"
    "- 不明確な情報は推定せず抽出しない。\n"
    "## 抽出例\n"
    "- 独立基礎\n"
    "- 基礎径 φ1200\n"
    "- 根入れ深さ 1.8m\n"
    "- 主筋 D16×12本\n"
    "- フープ筋 D13@200\n"
    "- 電柱挿入固定\n"
    "- 転倒防止補強\n"
)


def print_output(output: Output):
    """
    GPT-4oからのレスポンスを整形して表示

    Args:
        output: GPT-4oからのレスポンスモデル
    """
    print("- 抽出された特徴名称:")
    for item in output.items:
        print(f"    > {item}")


def save_to_file(output: Output, output_path: str):
    """
    GPT-4oからのレスポンスをJSONファイルに保存

    Args:
        output: GPT-4oからのレスポンスモデル
        output_path: 出力テキストファイルのパス
    """
    with open(output_path, "w") as f:
        f.write("- 抽出された特徴名称:\n")
        for item in output.items:
            f.write(f"    > {item}\n")
