"""
GPT-4oへの入力プロンプトとレスポンスモデルの定義

このモジュールは、Azure OpenAI GPT-4oモデルに送信するプロンプトと、
期待されるレスポンスの構造を定義します。
"""

from pydantic import BaseModel


class Output(BaseModel):
    """
    GPT-4oからのレスポンスモデル

    Attributes:
        description: 画像内容の説明文
    """

    items: list[str]


# 画像とOCR結果を入力とする場合のプロンプト
INPUT_PROMPT = (
    "## 提供するもの：\n"
    "- 画像一枚を与える。\n"
    "- OCRの結果は、文字とその文字を囲う4つの座標で構成される。\n"
    "- 座標は画素の場所を表し(x,y)で与えられる。原点は画像左上の角である。\n"
    "- 4つの座標は時計回りの順に並んでいる。\n"
    "## 前提 \n"
    "- 橋梁・高架構造物の柱頭部設計および施工図を重点的にチェックする構造設計・施工管理エンジニアです。\n"
    "## 質問 \n"
    "- 画像は、「柱頭部配筋図」です。\n"
    "- この図面から「柱頭部に特有の応力集中・定着条件・施工上の重要点を最も特徴づける情報」のみを、簡潔なキーワードとして抽出し、itemsに入れてください。\n"
    "## 抽出対象 \n"
    "- 柱頭部の主配筋・補強配筋（主鉄筋、帯鉄筋、フープ筋、スタッド、補強筋）\n"
    "- 応力集中に対応する配筋条件（密配筋、増径鉄筋、追加補強）\n"
    "- 定着・継手条件（定着長、折曲げ定着、機械式継手、重ね継手）\n"
    "- 主桁・梁との接合条件（主桁受け配筋、梁受け部補強）\n"
    "- 施工・組立に影響する指定（多段配筋、干渉注意、組立順序指定）\n"
    "## 注意点\n"
    "- 表記ゆれを正規化する。（例：「プレストレストコンクリート橋」→「PC橋」）\n"
    "- 数値は意味を持つもののみ記載（例：支間長 40m）\n"
    "- 同一内容は1回のみ抽出する。\n"
    "- 不明確な情報は推定せず抽出しない。\n"
    "## 抽出例\n"
    "- 柱頭部主鉄筋 D32\n"
    "- フープ筋 D13@100（密配筋）\n"
    "- 定着長確保\n"
    "- 折曲げ定着\n"
    "- 主桁受け部補強筋\n"
    "- 多段配筋\n"
)


def print_output(output: Output):
    """
    GPT-4oからのレスポンスを整形して表示

    Args:
        output: GPT-4oからのレスポンスモデル
    """
    print("- 抽出された特徴名称:")
    for item in output.items:
        print(f"    > {item}")


def save_to_file(output: Output, output_path: str):
    """
    GPT-4oからのレスポンスをJSONファイルに保存

    Args:
        output: GPT-4oからのレスポンスモデル
        output_path: 出力テキストファイルのパス
    """
    with open(output_path, "w") as f:
        f.write("- 抽出された特徴名称:\n")
        for item in output.items:
            f.write(f"    > {item}\n")
